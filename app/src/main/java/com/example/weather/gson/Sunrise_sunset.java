package com.example.weather.gson;

public class Sunrise_sunset {
    private String date;

    private String mr;

    private String ms;

    private String sr;

    private String ss;

    public void setDate(String date){
        this.date = date;
    }
    public String getDate(){
        return this.date;
    }
    public void setMr(String mr){
        this.mr = mr;
    }
    public String getMr(){
        return this.mr;
    }
    public void setMs(String ms){
        this.ms = ms;
    }
    public String getMs(){
        return this.ms;
    }
    public void setSr(String sr){
        this.sr = sr;
    }
    public String getSr(){
        return this.sr;
    }
    public void setSs(String ss){
        this.ss = ss;
    }
    public String getSs(){
        return this.ss;
    }
}
